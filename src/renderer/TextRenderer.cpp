bool TextRenderer::init() {
    generateBuiltinFont();
    return m_builtinTexture.isValid();
}

void TextRenderer::draw(SpriteBatch2D& batch, const std::string& text,
                        float x, float y, float scale,
                        const SpriteColor& color, float depth) const
{
    if (text.empty()) return;
    float charW = m_font.glyphW * scale;
    float charH = m_font.glyphH * scale;
    float spacing = m_font.spacing * scale;
    float cursorX = x, cursorY = y;
    for (char c : text) {
        if (c == '\n') { cursorX = x; cursorY += charH + spacing; continue; }
        if (c < 32) continue;
        SpriteRect uv = m_font.getGlyph(c);
        batch.draw(m_font.texture,
                   math::Vector2D(cursorX + charW * 0.5f, cursorY + charH * 0.5f),
                   math::Vector2D(charW, charH),
                   0.0f, math::Vector2D(0.5f, 0.5f), uv, color, depth);
        cursorX += charW + spacing;
    }
}

void TextRenderer::drawAligned(SpriteBatch2D& batch, const std::string& text,
                               float x, float y, TextAlign align,
                               float scale, const SpriteColor& color, float depth) const
{
    if (text.empty()) return;
    float textW = measureWidth(text, scale);
    float offsetX = 0.0f;
    switch (align) {
        case TextAlign::LEFT:   offsetX = 0.0f; break;
        case TextAlign::CENTER: offsetX = -textW * 0.5f; break;
        case TextAlign::RIGHT:  offsetX = -textW; break;
    }
    draw(batch, text, x + offsetX, y, scale, color, depth);
}

void TextRenderer::drawWrapped(SpriteBatch2D& batch, const std::string& text,
                               float x, float y, float maxWidth,
                               float scale, const SpriteColor& color, float depth) const
{
    if (text.empty() || maxWidth <= 0) return;
    float charW = m_font.glyphW * scale;
    float charH = m_font.glyphH * scale;
    float spacing = m_font.spacing * scale;
    float lineH = charH + spacing;
    float cursorX = x, cursorY = y;
    std::string word;
    for (size_t i = 0; i <= text.size(); i++) {
        char c = (i < text.size()) ? text[i] : ' ';
        if (c == ' ' || c == '\n' || i == text.size()) {
            float wordW = word.size() * (charW + spacing);
            if (cursorX + wordW > x + maxWidth && cursorX > x) {
                cursorX = x; cursorY += lineH;
            }
            for (char wc : word) {
                SpriteRect uv = m_font.getGlyph(wc);
                batch.draw(m_font.texture,
                           math::Vector2D(cursorX + charW * 0.5f, cursorY + charH * 0.5f),
                           math::Vector2D(charW, charH),
                           0.0f, math::Vector2D(0.5f, 0.5f), uv, color, depth);
                cursorX += charW + spacing;
            }
            cursorX += charW;
            if (c == '\n') { cursorX = x; cursorY += lineH; }
            word.clear();
        } else {
            word += c;
        }
    }
}

float TextRenderer::measureWidth(const std::string& text, float scale) const {
    float charW = m_font.glyphW * scale;
    float spacing = m_font.spacing * scale;
    float maxW = 0.0f, lineW = 0.0f;
    for (char c : text) {
        if (c == '\n') { maxW = std::max(maxW, lineW); lineW = 0.0f; }
        else { lineW += charW + spacing; }
    }
    return std::max(maxW, lineW);
}

float TextRenderer::measureHeight(const std::string& text, float scale) const {
    float charH = m_font.glyphH * scale;
    float spacing = m_font.spacing * scale;
    int lines = 1;
    for (char c : text) { if (c == '\n') lines++; }
    return lines * (charH + spacing);
}

